def get_active_constitution(route: str = "A") -> Dict[str, dict]:
    """根據選定路線回傳對應的 AI 憲法規則集。"""
    if route.upper() == "A":
        return CONSTITUTION_ROUTE_A
    elif route.upper() == "B":
        if not CONSTITUTION_ROUTE_B:
            print("⚠️  路線 B 尚未定義，請先完成 Lab 1-1 的 TODO。")
            return CONSTITUTION_ROUTE_A
        return CONSTITUTION_ROUTE_B
    else:
        raise ValueError(f"未知路線：{route}，請選擇 'A' 或 'B'。")


def display_constitution(constitution: Dict[str, dict]) -> None:
    """以格式化方式印出目前啟用的 AI 憲法。"""
    print("\n" + "=" * 60)
    print("  📜 目前啟用的 AI 憲法規則")
    print("=" * 60)
    for key, rule in constitution.items():
        print(f"\n  [{key}] {rule['name']}")
        print(f"    說明：{rule['description']}")
        print(f"    評分：{rule['score_criteria']}")
        print(f"    權重：{rule['weight']}")
    print("\n" + "=" * 60)
